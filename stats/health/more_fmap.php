		<h2>More Background: Federal Medical Assistance Percentage</h2><br/>

		<p>The Federal Medical Assistance Percentage (FMAP) reflects the federal government's share of state medical assistance programs.  It is determined annually by a formula that compares a state's average per capita income level with the national income average. States with a higher per capita income level are reimbursed a smaller share of their costs. By law, the FMAP cannot be lower than 50 percent or higher than 83 percent.  In Fiscal Year (FY) 2012, FMAPs averaged 58.8 percent overall andvaried from 50 percent in 19 states and the territories to 74.2 percent in Mississippi.  The Balanced Budget Act (BBA) of 1997 (Public Law105-33) permanently raised the FMAP for the District of Columbia from 50 to 70 percent.  For children covered through the CHIP program, the federal government pays states a higher share, or enhanced FMAP, which averaged 71.1 percent in FY 2012.</p><br/>

		<p>The federal government also reimburses states for100 percent of the cost of services provided to American Indians and Alaska Natives through facilities of the IndianHealth Service, for 100 percent of the cost of the QualifyingIndividuals (QI) program (described later), and for90 percent of the cost of family planning services, andshares in each state's expenditures for the administrationof the Medicaid program. Most administrative costs arematched at 50 percent, although higher percentages arepaid for certain activities and functions, such as developmentof mechanized claims processing systems.</p><br/>

		<p>Except for the CHIP program, the QI program, DSHpayments, and payments to territories, federal paymentsto states for medical assistance have no set limit(cap). Rather, the federal government matches (at FMAPrates) state expenditures for the mandatory services, aswell as for the optional services that the individual statedecides to cover for eligible beneficiaries, and matches(at the appropriate administrative rate) all necessary andproper administrative costs.</p><br/>

