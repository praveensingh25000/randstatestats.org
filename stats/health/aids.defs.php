		The criteria for an AIDS case has changed over time.  Prior to 1993, the presence of HIV infection and one of several opportunistic infections constituted the classification of a case as AIDS.  In 1993, the case definition changed to include CD4 Lymphocyte counts and percentages.
		CD4 are white blood cells that are a measure of the general immunocompetence of an individual. The quantity of these CD4 cells drops as an individual becomes immunocomprimised. The new case definition in 1993 specified that CD4 counts less than per mm3 OR less than 14% of lymphocytes were sufficiently low to classify an individual as an AIDS case.  This provided more objective criteria for identifying AIDS cases and also tended to classify cases somewhat earlier in the course of HIV infection in that some time usually elapsed between the drop of CD4 cells to that level and the acquisition of an AIDS-defining condition.
		</p><br/>

		<p>
		For this reason, you will note in 1993 and 1994 a sudden upsurge in AIDS cases - this is not due to a sudden outbreak or alteration in the spread of the disease, but rather the change in the case definition that took place during that time.
