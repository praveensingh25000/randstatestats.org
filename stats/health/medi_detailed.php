<p>HCE includes these 10 PHC expenditure types and adds two: administration and the net cost of private insurance, and public health activity.  Administration reflects the cost of running government health care programs.  The net cost of private insurance reflects the difference between premiums earned by insurers and the claims or losses incurred for which insurers become liable.  Administration and the net cost of private insurance account for about 7 percent of total health care spending, and public health activity reflect about 3 percent of that total.</p><br/>

<p>Finally, NHE, the broadest expenditure category, adds investment in research, structures, and equipment.  This includes spending for noncommercial biomedical research and expenditures by health care establishments on structures and equipment.  Investment reflects about 6 percent of total health care expenditures.  The figure below illustrates the relationship among NHE, HCE, and PHC.</p><br/>
