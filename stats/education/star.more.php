		<p>During the 1998-99 school year, multiple-choice questions were developed specifically to assess the California English-Language Arts and Mathematics Content Standards.  The California Standards Tests (CSTs) evolved from these intitial content standards tests.  The purpose of the CSTs is to determine students' achievement of the California Academic Content Standards for each grade or course.  The CSTs are a major component of California's accountability system for schools and districts. CST and CAPA results are the major component used for calculating each school's Academic Performance Index (API).  These results are also used for determining if elementary and middle schools are making adequate yearly progress in helping all students become proficient on the state's academic content standards as required by the federal No Child Left Behind Act of 2001.  High school students may also use CST results to qualify for the California Golden State Seal Merit Diploma.</p><br/>

